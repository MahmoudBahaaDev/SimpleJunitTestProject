package com.unit.test;

public class TestLogic {

	/**
	 * this method is used for multiplying two numbers .
	 * 
	 * @param number1
	 * @param number2
	 * @return int
	 */
	public int multiply(int number1, int number2) {
		return number1 * number2;
	}

	/**
	 * this method is used for adding two numbers .
	 * 
	 * @param number1
	 * @param number2
	 * @return int
	 */
	public int testAdd(int number1, int number2) {
		return number1 + number2;
	}
}
